public class ForEach_Demo {
    public static void main(String[] args) {
        String[] str = {"Hùng","Dũng","Sang","Trọng"};
        System.out.println("Danh sách:");
        for (String item : str) {
            System.out.println(item);
        }
        System.out.println("\n");
        
    }
}
